package sirat;

import java.io.*;

public class FriendListV 
{
    private String nam;
    private String pho;
   
    public FriendListV(String na, String ph)
    {
        nam = na;
        pho = ph;
    }
    
    public String getName()
    {
        return nam;
    }
    
    public String getPhone() 
    {
        return pho;
    }
 
}
